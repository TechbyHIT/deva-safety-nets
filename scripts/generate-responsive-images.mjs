/**
 * Build-time responsive WebP variants for /public/images photos.
 * Outputs: photo.jpg → photo.w640.webp, photo.w1280.webp + manifest for runtime srcSet.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const IMAGES_DIR = path.join(ROOT, "public", "images");
const MANIFEST_PATH = path.join(ROOT, "src", "lib", "responsive-image-manifest.ts");
const WIDTHS = [640, 1280];
const IMAGE_EXT = /\.(jpe?g|png|webp)$/i;
const SKIP = /\.w\d+\.webp$/i;

const manifest = {};

async function processFile(filePath) {
  const rel = path.relative(IMAGES_DIR, filePath).replace(/\\/g, "/");
  const publicSrc = `/images/${rel}`;
  const widths = [];

  for (const width of WIDTHS) {
    const out = filePath.replace(/\.[^.]+$/, `.w${width}.webp`);
    await sharp(filePath, { failOn: "none" })
      .rotate()
      .resize(width, null, { withoutEnlargement: true, kernel: sharp.kernel.lanczos3 })
      .webp({ quality: 82, effort: 4 })
      .toFile(out);
    widths.push(width);
    console.log(`Wrote images/${rel.replace(/\.[^.]+$/, `.w${width}.webp`)}`);
  }

  if (widths.length) manifest[publicSrc] = widths;
  return widths.length;
}

async function walk(dir) {
  let total = 0;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      total += await walk(full);
      continue;
    }
    if (!IMAGE_EXT.test(entry.name) || SKIP.test(entry.name)) continue;
    try {
      total += await processFile(full);
    } catch (err) {
      console.warn(`Skip ${full}: ${err.message}`);
    }
  }
  return total;
}

function writeManifest() {
  const body = `/** Auto-generated by scripts/generate-responsive-images.mjs — do not edit. */
export const RESPONSIVE_IMAGE_MANIFEST: Record<string, number[]> = ${JSON.stringify(manifest, null, 2)};
`;
  fs.writeFileSync(MANIFEST_PATH, body);
  console.log(`Wrote ${path.relative(ROOT, MANIFEST_PATH)} (${Object.keys(manifest).length} images)`);
}

async function main() {
  if (!fs.existsSync(IMAGES_DIR)) {
    writeManifest();
    console.log("No public/images — wrote empty responsive manifest");
    return;
  }
  const total = await walk(IMAGES_DIR);
  writeManifest();
  console.log(`\nGenerated variants for ${Object.keys(manifest).length} images (${total} widths)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
